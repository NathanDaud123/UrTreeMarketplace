// Function to seed users
async function seedUsers() {
  let successCount = 0;
  for (const userData of sampleUsers) {
    try {
      if (userData.role === 'admin') {
        await userAPI.register(userData.email, userData.password, userData.name);
      } else if (userData.role === 'seller' && userData.hasSellerAccount) {
        // Register as buyer first
        await userAPI.register(userData.email, userData.password, userData.name);
        // Then apply as seller
        await userAPI.applySeller(userData.email, {
          shopName: userData.shopName,
          shopDescription: userData.shopDescription,
          address: userData.shopAddress,
          city: userData.shopCity,
          phone: userData.phone,
        });
      } else {
        // Register as buyer
        await userAPI.register(userData.email, userData.password, userData.name);
      }
      successCount++;
    } catch (error) {
      console.log('User might already exist:', userData.email);
    }
  }
  console.log(`âœ… Seeded ${successCount} users`);
}

