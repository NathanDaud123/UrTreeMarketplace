// Check if database needs seeding
export async function checkAndSeedData() {
  try {
    const { products } = await productAPI.getAll();
    
    // If few products exist, seed the database
    if (!products || products.length < 10) {
      console.log('Database needs seeding. Starting seed process...');
      await seedInitialData();
    } else {
      console.log(`Database already has ${products.length} products`);
    }
  } catch (error) {
    console.error('Error checking database:', error);
    // If there's an error fetching products, try seeding anyway
    await seedInitialData();
  }
}
